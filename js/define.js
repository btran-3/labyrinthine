//definition buttons

var
	showBtn1 = document.querySelector("#test-para button:nth-of-type(1)"),
	content1 = document.querySelector("#test-para > div:nth-of-type(1)");

showBtn1.addEventListener("click", function(){
  if(content1.className == "hidden"){
	content1.className = "visible";
} else{
	content1.className = "hidden";
}
}, false);


